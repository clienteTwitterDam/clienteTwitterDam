    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            dataSource = new JRBeanCollectionDataSource(GestionTwitter.listaTweetsPropiosInforme(twitterConnection));
            parametros = new HashMap();
            print = JasperFillManager.fillReport("reports/jasper/informeTwitter.jasper", parametros, dataSource);
            JasperExportManager.exportReportToPdfFile(print, "reports/pdf/informeTwitter" + ".pdf");
        } catch (JRException ex) {
            Logger.getLogger(PantallaInformes.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Informe guardado en \"reports/pdf\" con éxito");
    }//GEN-LAST:event_jButton1ActionPerformed

