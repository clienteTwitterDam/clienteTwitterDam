    private void jBTweetsUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTweetsUserActionPerformed
        String usuario = JOptionPane.showInputDialog("Introduzca un usuario");
        
        if (usuario.equals("") || usuario == null) {
            JOptionPane.showMessageDialog(this, "Tiene que escoger un usuario");
        } else {
            try {
                dataSource = new JRBeanCollectionDataSource(GestionTwitter.listaTweetsUsuarioInforme(twitterConnection, usuario));
                parametros = new HashMap();
                print = JasperFillManager.fillReport("reports/jasper/tweetsUsuario.jasper", parametros, dataSource);
                JasperExportManager.exportReportToPdfFile(print, "reports/pdf/informeTweetsUsuario" + ".pdf");
            } catch (JRException ex) {
                Logger.getLogger(PantallaInformes.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Informe guardado en \"reports/pdf\" con éxito");
        }
    }//GEN-LAST:event_jBTweetsUserActionPerformed


